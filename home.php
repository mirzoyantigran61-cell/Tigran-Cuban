    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const carousel = document.getElementById('featured-carousel');
            if (!carousel) return;
            
            const slides = carousel.querySelectorAll('.featured-slide');
            const indicators = carousel.querySelectorAll('.carousel-indicator');
            
            if (slides.length <= 1) return; // No hay necesidad de rotar si solo hay un juego
            
            let currentSlide = 0;
            let autoRotateInterval;
            
            function showSlide(index) {
                // Ocultar todos los slides
                slides.forEach(slide => {
                    slide.classList.remove('active');
                });
                
                // Actualizar indicadores
                indicators.forEach((indicator, i) => {
                    if (i === index) {
                        indicator.classList.add('bg-blue-500', 'w-8');
                        indicator.classList.remove('bg-slate-400', 'dark:bg-gray-600');
                    } else {
                        indicator.classList.remove('bg-blue-500', 'w-8');
                        indicator.classList.add('bg-slate-400', 'dark:bg-gray-600');
                    }
                });
                
                // Mostrar slide actual
                slides[index].classList.add('active');
                currentSlide = index;
            }
            
            function nextSlide() {
                const next = (currentSlide + 1) % slides.length;
                showSlide(next);
            }
            
            // Auto-rotación cada 4 segundos
            function startAutoRotate() {
                autoRotateInterval = setInterval(nextSlide, 4000);
            }
            
            function stopAutoRotate() {
                if (autoRotateInterval) {
                    clearInterval(autoRotateInterval);
                }
            }
            
            // Agregar click a indicadores
            indicators.forEach((indicator, index) => {
                indicator.addEventListener('click', () => {
                    stopAutoRotate();
                    showSlide(index);
                    startAutoRotate(); // Reiniciar auto-rotación
                });
            });
            
            // Pausar rotación al hover
            carousel.addEventListener('mouseenter', stopAutoRotate);
            carousel.addEventListener('mouseleave', startAutoRotate);
            
            // Iniciar auto-rotación
            startAutoRotate();
        });
    </script>
